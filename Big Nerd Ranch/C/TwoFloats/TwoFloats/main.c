#include <stdio.h>

int main(int argc, const char * argv[]) {
    // Declare two floats
    float pi = 3.14, life = 42.0;
    
    // Declare double as sum of floats
    double sumFloats = pi + life;
    
    // Print result
    printf("The result of pi + life equals %f.\n", sumFloats);
    
    
}
